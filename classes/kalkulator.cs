class Kalulator : Ikalkulator
{
    public double AddNumber(double Input1, double Input2){return Input1 + Input2;}
    public double SubtractNumber(double Input1, double Input2){return Input1 - Input2;}
    public double MultiplyNumber(double Input1, double Input2){return Input1 * Input2;}
    public double DivideNumber(double Input1, double Input2){return Input1 / Input2;}


}
